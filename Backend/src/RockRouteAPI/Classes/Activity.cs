using System;

namespace RockRoute.Classes
{
    public class Activity
    {

        private string _name;
        private DateTime? _date;
        private string _notes;


        public string Name
        {
            get => _name;
            set { _name;}
        }

        public DateTime? Date 
        {
            get => _date;
            set { _date;}
        }

        public string Notes 
        {
            get => _notes:
            set { _notes;}
        }

        public 

        public Activity(string Name, DateTime Date, Notes) {
            this._date = Date
            this._name = Name
            this._notes = Notes
        }



    }
}